number = int(input("Введіть 5-значне число: "))

# Знаходимо кожну цифру
a = number // 10000        # перша цифра
b = (number // 1000) % 10  # друга цифра
c = (number // 100) % 10   # третя цифра
d = (number // 10) % 10    # четверта цифра
e = number % 10            # п'ята цифра

# Виводимо цифри у зворотному порядку
reversed_number = e*10000 + d*1000 + c*100 + b*10 + a
print(reversed_number)